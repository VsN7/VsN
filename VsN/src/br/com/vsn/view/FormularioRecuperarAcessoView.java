    private void salvarButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButton3ActionPerformed
        if(!inputSenha.getText().isEmpty() && !inputSenhaNovamente.getText().isEmpty() && !inputPalavraSeguranca.getText().isEmpty() ){
            if(inputSenha.getText().equals(inputSenhaNovamente.getText())){
                UsuarioController uc = new UsuarioController();
                try {
                    this.encriptografarPalavraSegurancaUsuario(inputSenha.getText());
                } catch (Exception ex) {
                    Logger.getLogger(FormularioRecuperarAcessoView.class.getName()).log(Level.SEVERE, null, ex);
                }
                RecuperarAcessoView.usuario.setSenha(senha);
                uc.editUsuario(RecuperarAcessoView.usuario);
                JOptionPane.showMessageDialog(null, "Senha alterada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                LoginView lv;
                try {
                    this.dispose();
                    lv = new LoginView();
                    lv.setVisible(true);
                } catch (Exception ex) {
                    Logger.getLogger(MenuView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(null, "As senhas não estão iguais!", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_salvarButton3ActionPerformed

